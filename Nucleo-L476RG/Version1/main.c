// Called when the buffer is completely full
void HAL_DFSDM_FilterRegConvCpltCallback(DFSDM_Filter_HandleTypeDef *hdfsdm_filter)
{
	buffer_full = true;
	int i, j = 0;

	// Once the buffer is full, stop DMA
	HAL_DFSDM_FilterRegularStop_DMA	(hdfsdm_filter);

	// Process the data
	// For each value in buff(2 samples) get 1 value in processed_buff(1 sample)
    if (flag)
    {

    	for (i = 0; i < BUFFER_SIZE; i++){
    		for(j = 0; j < 2; j++){
    			processed_buff[i*2 + j] = (j==0 ? ((Buff[i] & 0xFFFF0000) >> 16) : (Buff[i] & 0x0000FFFF)) >> 4;
    		}
    	}

    	for(i=0; i<BUFFER_SIZE*2;i++){
    		for(j=0; j<2; j++){
    			sender_buffer[i*2 + j] = (j==0 ? (processed_buff[i] >> 8) : (uint8_t)processed_buff[i]);
    		}
    	}

        flag = false;
    }

    // TODO: Restart DMA and set circular mode

}




